"""
bls/gap/witness_eval.py — ground-truth evaluation of a generator-index word in GAP.

The witness lift turns a `Factorization` (a word in generator indices) back into a
period word `v` by reading the indices left-to-right through the transition monoid
(`witness._induced_transform`). That reading is correct only if the Python image-list
composition matches GAP's right-action multiplication; if it does not, `v` comes out
reversed (research note §4 gotcha). A period-2 cycle hides the bug (it is its own
inverse); pinning the convention needs an independent oracle for "what transformation
does this index word denote", and that oracle is GAP itself.

`eval_word(gens, word)` returns the transformation GAP computes for the word
`gens[word[0]] * gens[word[1]] * …` — GAP's right action — as a 0-based image list,
directly comparable to `witness._induced_transform`. A new sibling of
`witness_group.py`; it reuses the stable GAP infrastructure read-only.
"""

from __future__ import annotations

from typing import List

from .export import _format_transformation
from .runner import run_gap_script


def _eval_script(generators: List[List[int]], word: List[int]) -> str:
    """A self-contained GAP script: fold the (1-based) index `word` into a product of
    the generator transformations and print its image list over `[1..n]`."""
    body = ",\n    ".join(_format_transformation(g) for g in generators)
    one_based = ", ".join(str(i) for i in word)  # indices are already 1-based
    n = len(generators[0])
    return (
        "# Auto-generated by BuchiToLTL/bls/gap/witness_eval.py (word evaluation).\n"
        f"gens := [\n    {body}\n];\n"
        f"word := [ {one_based} ];\n"
        "prod := gens[word[1]];\n"
        "for k in [2 .. Length(word)] do prod := prod * gens[word[k]]; od;\n"
        f"Print(\"IMAGE: \", List([1 .. {n}], i -> i ^ prod), \"\\n\");\n"
        "QUIT;\n"
    )


def eval_word(
    generators: List[List[int]],
    word: List[int],
    *,
    gap_cmd: str = "gap",
    timeout: int = 60,
) -> List[int]:
    """Return the 0-based image list of the transformation `gens[w0] * … * gens[wk]`
    as GAP evaluates it (right action), for the 1-based generator-index `word`.

    `generators` is the list-of-images representation `extract_generators` produces;
    `word` is a non-empty list of 1-based indices into it (the `Factorization`
    convention). Raises if GAP prints no `IMAGE:` line.
    """
    if not generators:
        raise ValueError("at least one generator is required")
    if not word:
        raise ValueError("word must be non-empty")
    raw = run_gap_script(
        _eval_script(generators, word), gap_cmd=gap_cmd, timeout=timeout
    )
    for line in raw.splitlines():
        if line.startswith("IMAGE:"):
            inside = line.partition(":")[2].strip().strip("[]")
            return [int(tok) - 1 for tok in inside.replace(" ", "").split(",") if tok]
    raise RuntimeError(f"witness-eval GAP reported no IMAGE; output:\n{raw[:400]}")


__all__ = ["eval_word"]
